    /* show GUI */
    SnippetsDlg * main_win = new SnippetsDlg (NULL);
    main_win->setWindowFlags(Qt::Window);
#	if defined(Q_OS_SYMBIAN)
    main_win->showMaximized();
#	else
    main_win->show();
#	endif

